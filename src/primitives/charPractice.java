package primitives;

public class charPractice {
    public static void main(String[] args) {
        char letter = 'A';
        System.out.println("This is first letter " + letter); // A or 65

        char symbolNum = '3';
        System.out.println("this is symbol" + symbolNum); //3
        System.out.println(letter+symbolNum); //A3 65+51 = 116

        // int symbol1 = 65;
        // System.out.println(symbol1); // A or 65
        char symbol1 = 65;
        System.out.println(symbol1);

        char symbol2 = 75;
        System.out.println(symbol2); //K

        System.out.println(symbol1+symbol2); //140
    }
}
