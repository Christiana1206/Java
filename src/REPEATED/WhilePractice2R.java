package REPEATED;

public class WhilePractice2R {
    public static void main(String[] args) {
         /*
        TASK:
        print out even numbers between 20 and 40(not including 20 and 40)
         */

        int num1 = 20, num2 = 40;
        while (num1<=num2) {
            System.out.println(num1);
            num1+=2;
        }







    }
}
